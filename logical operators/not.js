// result = !value ; şeklinde gösterilir. 

/*
-operatör tek operanddan oluşur
-operand değerini boolean tipine çevirir true / false 
-tersine döndürür.
*/
console.log( !true );  // false     
console.log( !false );      // true
console.log( !0 );      // true
  

// çift değil işareti değeri boolean tipine çevirir.   
console.log(!!"hello I am Daniel");     // true
console.log(!!null);            // false

