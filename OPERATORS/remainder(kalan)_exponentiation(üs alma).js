// REMAİNDER (KALAN)

// kalan ½ operatörü yüzde ile bir alakası yoktur. 
// a ½ b a'nın b'ye bölümünden kalanı verir.  


alert(5 % 2); // 5' in 2 ile bölümünden kalan 1
alert(8 % 3);  // 2 
alert(6 % 3);   // 0 






// EXPONENTİATİON ( ÜS ALMA)

// JS ye sonradan eklenen bir dildir.
// a**b şeklinde gösterilir.

alert(2 ** 2);  // 4 
alert(2 ** 3);  // 8 
alert(2 ** 4);  // 16

// integer olmayan değerler içinde aynı işlem yapılabilir.  

alert( 4 ** (1/2)); // 2  (1/2 üstü karekök anlamına gelir )
alert( 8 ** (1/3));  // 2  (1/3 üstü küp kök anlamına gelir .)

